package org.gs1.smartcity.datatype;

public class ParameterType {
	
	private String name;
	private String value;
	
	public String getName() {
		
		return name;
	}
	
	public void setName(String value) {
		
		this.name = value;
	}
	
	public String getValue() {
		
		return value;
	}
	
	public void setValue(String value) {
		
		this.value = value;
	}

}
