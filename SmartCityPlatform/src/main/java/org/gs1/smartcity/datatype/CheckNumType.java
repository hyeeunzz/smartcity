package org.gs1.smartcity.datatype;

public class CheckNumType {
	
	private String type;
	private int checkNum;
	
	public String getType() {
		
		return type;
	}
	
	public void setType(String value) {
		
		this.type = value;
	}
	
	public int getCheckNum() {
		
		return checkNum;
	}
	
	public void setCheckNum(int value) {
		
		this.checkNum = value;
	}

}
